        		<h2><b>Telecomunicazioni</b></h2> <!-- Titolo -->
        		<h4>Telecomunicazioni, abbreviazione TLC, è l'attività di trasmissione a lunga distanza di segnali,
					parole e immagini sotto forma di messaggi tra due o più soggetti, detti mittente e destinatari,
					mediante dispositivi elettronici (trasmettitore e ricevitore) attraverso un canale fisico di comunicazione.
					Le vaste applicazioni tecnologiche delle telecomunicazioni sono riconducibili a due gruppi: le tecnologie dell'informazione
					e le tecnologie dell'informazione e della comunicazione, la cui ampia diffusione nell'attuale società ha dato vita
					- attraverso mezzi di comunicazione di massa come telefono, radio e televisione - all'attuale società dell'informazione,
					nonché uno dei settori trainanti della cosiddetta terza rivoluzione industriale.
